def upper(line_input):
    '''Принимает строку и возвращает ее заглавными буквами'''
    line_input = input()
    return line_input.upper()

def capital_letters(line_input):
    '''делает заглавными первые буквы каждого слова в строке, поступившей на вход функции'''
    line_input = input()
    return line_input.title()